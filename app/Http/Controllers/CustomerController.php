     */
    public function store(Request $request)
    {
        $validated = $request->validate([
            'name'=>'required',
           'email'=>'required|unique:users|min:5',
           'password'=>'required|password',
        ]);
        
        $request['type'] = 'customer';
        
        User::create($request->all());
